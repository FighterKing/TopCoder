class CreateGroups {
public:
  int minChanges(vector <int> groups, int minSize, int maxSize) {
    int sum = accumulate(groups.begin(), groups.end(), 0);
    int size = groups.size();
    if (sum > maxSize * size || sum < minSize * size)
      return -1;
    int count1 = 0, count2 = 0;
    for (auto i : groups) {
      if (i > maxSize)
        count1 += i - maxSize;
      else if (i < minSize)
        count2 += minSize - i;
      
    }
    return max(count1, count2);
  }
};


