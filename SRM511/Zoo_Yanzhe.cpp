class Zoo {
public:
	long long theCount(vector <int> answers) {
		sort(begin(answers), end(answers));
		if (answers[0] != 0) return 0;

		int N = answers.size();
		int i;
		for (i = 0; i < N && answers[i] == i / 2; i++) {}
		int j;
		for (j = i; j < N; j++)
			if (answers[j-1] + 1 != answers[j])
				return 0;
		return pow(2, i / 2 + (i / 2 * 2 == N ? 0 : 1)); 
	}
};


