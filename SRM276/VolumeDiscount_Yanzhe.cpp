class VolumeDiscount {
private:
	vector<int> memo;
	int searchBestDeal(const vector<pair<int, int> > &pList, int quantity) {

		int bestForOver = numeric_limits<int>::max();

		if (quantity <= 0) return 0;

		if (memo[quantity] != -1)
			return memo[quantity];

		for (auto p : pList) {
			if (p.first > quantity && p.second < bestForOver)
				bestForOver = p.second;
		}

		int ret = bestForOver;
		for (auto p : pList) {
			if (p.first <= quantity)
				ret = min(ret, p.second + searchBestDeal(pList, quantity - p.first));
		}

		memo[quantity] = ret;

		return ret;
	}
public:
	int bestDeal(vector <string> priceList, int quantity) {
		vector<pair<int, int> > pList;
		for (auto s : priceList) {
			int sp;
			for (sp = 0; sp < s.size() && s[sp] != ' '; sp++) {}
			pList.push_back(make_pair(stoi(s.substr(0, sp)), stoi(s.substr(sp+1))));
		}
		memo.resize(quantity + 1, -1);
		return searchBestDeal(pList, quantity);
	}
};


