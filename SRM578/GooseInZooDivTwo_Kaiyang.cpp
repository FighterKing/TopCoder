class GooseInZooDivTwo {
public:
  int count(vector <string> field, int dist) {
    const long unsigned int V = 1000000007;
    int columnSize = field[0].size();
    int rowSize = field.size();
    
    int countNum = 0;
    vector<vector<bool>> v(field.size(), vector<bool>(field[0].size(), false));
    vector<pair<int, int>> toUpdate;
    for (int i = 0; i < rowSize; ++i) {
      for (int j = 0; j < columnSize; ++j) {
        if (!v[i][j] && field[i][j] == 'v') {
          ++countNum;
          v[i][j] = true;
          toUpdate.push_back(make_pair(i, j));
          // BFS
          while (!toUpdate.empty()) {
            pair<int, int> p = toUpdate.back();
            toUpdate.pop_back();
            for (int ii = 0; ii < rowSize; ++ii) {
              for (int jj = 0; jj < columnSize; ++jj) {
                if (!v[ii][jj] && field[ii][jj] == 'v' && abs(p.first - ii) + abs(p.second - jj) <= dist) {
                  v[ii][jj] = true;
                  toUpdate.push_back(make_pair(ii, jj));
                }
              }
            }
          }
        }
      }
    }
    
    long long re = 1;
    for(int i = 0; i < countNum; i++)
      re = (re * 2) % V;

    re = (re - 1 + V) % V;
    
    return re;
  }
};


